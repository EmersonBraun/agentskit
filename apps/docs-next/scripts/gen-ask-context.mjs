#!/usr/bin/env node
// Build a slim docs corpus for the Ask-the-docs widget.
// Uses each MDX's frontmatter title + description + first H2 headings. Keeps total under ~12KB.
import { readFileSync, readdirSync, writeFileSync, mkdirSync } from 'node:fs'
import { dirname, join, resolve, relative } from 'node:path'
import { fileURLToPath } from 'node:url'

const __dirname = dirname(fileURLToPath(import.meta.url))
const ROOT = resolve(__dirname, '..')
const DOCS = resolve(ROOT, 'content/docs')
const OUT = resolve(ROOT, 'lib/ask-context.ts')
const SITE = 'https://www.agentskit.io'

const SKIP_DIRS = new Set(['api', 'for-agents'])
const MAX_DESC = 160
const MAX_HEADINGS = 4

function walk(dir) {
  const out = []
  for (const e of readdirSync(dir, { withFileTypes: true })) {
    if (e.isDirectory()) {
      if (SKIP_DIRS.has(e.name)) continue
      out.push(...walk(join(dir, e.name)))
    } else if (e.name.endsWith('.mdx') || e.name.endsWith('.md')) {
      out.push(join(dir, e.name))
    }
  }
  return out
}

const pages = []
for (const file of walk(DOCS)) {
  const raw = readFileSync(file, 'utf8')
  const fm = raw.match(/^---\n([\s\S]*?)\n---/)
  if (!fm) continue
  const block = fm[1]
  const title = block.match(/^title:\s*(.+)$/m)?.[1]?.replace(/^['"]|['"]$/g, '')?.trim()
  const description = block.match(/^description:\s*(.+)$/m)?.[1]?.replace(/^['"]|['"]$/g, '')?.trim()
  if (!title) continue

  const body = raw.slice(fm[0].length)
  const headings = [...body.matchAll(/^##\s+(.+)$/gm)]
    .map((m) => m[1].trim().replace(/`/g, ''))
    .slice(0, MAX_HEADINGS)

  const slug = relative(DOCS, file).replace(/\\/g, '/').replace(/\.(mdx|md)$/, '').replace(/\/index$/, '')
  const url = slug ? `${SITE}/docs/${slug}` : `${SITE}/docs`
  pages.push({ url, title, description, headings })
}

pages.sort((a, b) => a.url.localeCompare(b.url))

function truncate(s, n) {
  if (!s) return ''
  return s.length <= n ? s : s.slice(0, n - 1).replace(/\s+\S*$/, '') + '…'
}

const lines = []
for (const p of pages) {
  const desc = truncate(p.description ?? '', MAX_DESC)
  const head = p.headings.length ? ' — ' + p.headings.join(' · ') : ''
  lines.push(`- [${p.title}](${p.url.replace('https://www.agentskit.io', '')})${desc ? ': ' + desc : ''}${head}`)
}
const corpus = lines.join('\n')

const ts = `// Auto-generated by scripts/gen-ask-context.mjs — do not edit by hand.
export const DOCS_CORPUS = ${JSON.stringify(corpus)}
export const DOCS_CORPUS_PAGES = ${pages.length}
export const DOCS_CORPUS_BYTES = ${Buffer.byteLength(corpus, 'utf8')}
`

mkdirSync(dirname(OUT), { recursive: true })
writeFileSync(OUT, ts)
console.log(`ask-context: ${pages.length} pages, ${Buffer.byteLength(corpus, 'utf8')} bytes → ${relative(ROOT, OUT)}`)
